console.log("==========Swapping of 2 variables using 3rd variable==========");

var sweety;
sweety = "Sweety";
var cutie;
cutie = "Cutie";
console.log("=====Before Swap=====");
console.log("Name 1:",sweety, "Name 2:",cutie);

console.log("=====After Swap=====");
var temp = sweety;
sweety = cutie;
cutie = temp;
console.log("Name 1:",sweety, "Name 2:",cutie);



